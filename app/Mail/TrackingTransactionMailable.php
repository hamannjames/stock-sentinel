<?php

namespace App\Mail;

use App\Models\Transaction;
use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Illuminate\Contracts\Queue\ShouldQueue;

// class for generating transaction email simply accepts a transaction in constructor and returns
// markdown view. The sending is done behind the scenes in mailable class
class TrackingTransactionMailable extends Mailable
{
    use Queueable, SerializesModels;

    public $transaction;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct(Transaction $transaction)
    {
        $this->transaction = $transaction;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        // this returns a view in the views folder under tracking, which is a markdown view
        return $this->markdown('tracking.transaction-made');
    }
}
